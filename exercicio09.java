package estruturasRepetitivas;

import java.util.Scanner;

public class ecercicio09 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		System.out.println("informe o número:");
		int n = sc.nextInt();

		for (int i = 1; i <= i; i++) {
			if (n % i == 0) {
				System.out.println(i);
			}

			sc.close();
		}

	}
}
